'''
ФИПИ
Алгоритм вычисления значения функции F(n), где n, - натуральное число, задан так:

F(n)=0, при n <= 1
F(n) = F(n - 1) + 3n^2, при n > 1 и n - нечётно
F(n) = n/2 + F(n -1 ) +2, при n > 1 и n-чётно

Чему равно значение функции F (49)?
'''

F = [0] * 50

for n in range(len(F)):
    if n <= 1:
        F[n] = 0
    elif n > 1 and n % 2 != 0:
        F[n] = F[n-1] + 3*(n**2)
    elif n > 1 and n % 2 == 0:
        F[n] = (n//2) + F[n-1] + 2

print(F[49])
